    /**
     * Updates car in database
     * @param evt
     */
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        try {
            int id = Integer.parseInt(model.getValueAt(row, 0).toString());
            Car car = new Car();
            CarManager cm = new CarManagerImpl();
            car = cm.findCarById(id);

            CarUpdateJFrame cuf = new CarUpdateJFrame(car, this);
            cuf.setVisible(true);
            Toolkit tk = Toolkit.getDefaultToolkit();
            Dimension screenSize = tk.getScreenSize();
            int screenHeight = screenSize.height;
            int screenWidth = screenSize.width;
            cuf.setSize(screenWidth / 4, screenHeight / 4);
            cuf.setLocation(screenWidth / 3, screenHeight / 3);
        } catch (ArrayIndexOutOfBoundsException aiobe) {
            JOptionPane.showMessageDialog(this, languageTexty.getString("select_car"));
        }
    }//GEN-LAST:event_jButton2ActionPerformed

