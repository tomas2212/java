    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        int row = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        try {
            int id = Integer.parseInt(model.getValueAt(row, 0).toString());
            int response = JOptionPane.showConfirmDialog(this, languageTexty.getString("delete_message"));
            if (response == 0) {
                Car car = new Car();
                CarManager cm = new CarManagerImpl();
                car = cm.findCarById(id);
                cm.deleteCar(car);
                createCarsTable();
            }
        } catch (ArrayIndexOutOfBoundsException aiobe) {
            JOptionPane.showMessageDialog(this, languageTexty.getString("select_car"));
        }
    }//GEN-LAST:event_jButton13ActionPerformed

