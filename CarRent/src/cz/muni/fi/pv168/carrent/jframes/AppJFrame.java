    /**
     * Clears table
     * @param jtable that is to be cleared
     * @return model of cleared table
     */
    public DefaultTableModel clearTable(JTable jtable) {
        DefaultTableModel model = (DefaultTableModel) jtable.getModel();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        return model;
    }

